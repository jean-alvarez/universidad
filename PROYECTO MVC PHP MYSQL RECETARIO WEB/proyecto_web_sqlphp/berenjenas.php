    <div id="padre">
        <div id="izquierda">
           
            <ol>
                <p><u>Ingredientes</u></p>
                <li type="disc">2 Berenjenas</li>
                <li type="disc">250 ml de leche</li>
                <li type="disc">150 gramos de harina</li>
                <li type="disc">2 dientes de ajo</li>
                <li type="disc">1 rama de perejil</li>
                <li type="disc">Salsa de soja</li>
                <li type="disc">Aceite y sal</li>
                <li type="disc">Una lata de cerveza fria</li>
            </ol>
            </div>
            <div id="derecha">
                <p id="p3"><strong><u>
                    Preparación:
                    </u>
                </strong></p>
                <ol >
                <li>Comenzamos lavando la berenjena, retirar el tallo y cortar en rodajas de grosor medio, es decir, ni muy finas ni muy gruesas.</li>
                <li>Colocarlas en un escurridor, añadir una pizca de sal y dejar unos minutos que se escurran para que suelten su jugo y pierdan el sabor amargo.</li>
                <li>En un bol colocar la cerveza, e introduce en ella las rodajas de berenjena, sin retirar la sal que añadiste.</li>
                <li>Déjalas a remojo durante 10-15 minutos para que se impregnen bien del líquido y tomen su sabor.</li>
                <li>Luego pasa las rodajas de berenjena por harina para rebozarlas bien, procura cubrirlas por completo, así quedarán más crujientes.</li>
                <li>En una sarten con bastante aceite freir las berenjenas bien de ambos lados.</li>
                <li>Colocar sobre papel absorbente para retirar el exceso de aceite y sigue cocinando hasta acabar con todas las rodajas.</li>
                </ol>
