                <p><strong><u>
                    Preparación:
                    </u>
                </strong></p>
                <ol>
                    <li>Lava los pimientos, colócalos en una fuente apta para el horno, sazónalos y riégalos con un poco de aceite. Introdúcelos en el horno a 200º C, durante 25 minutos. Deja que se templen y pélalos.</li>
                    <li>Pica la berza y ponla a cocer (8 minutos) en una cazuela con agua y una pizca de sal.</li>
                    <li>Para la salsa, pica una cebolleta y 2 dientes de ajo (finamente) y pon a pochar en una cazuela con un poco de aceite. Pica las setas e incorpóralas. Vierte el vino blanco y un poco de agua, sazona y cocina durante 10 minutos.</li>
                    <li>Pica 3 cebolletas, los pimientos y las judías en juliana fina. Pela las zanahorias y saca tiras finas con un pela patatas.</li>
                    <li>Pica 2 dientes de ajo en láminas, pon a dorar en una sartén con aceite, agrega las verduras y cocínalas.</li>
                    <li>Rellena los pimientos con la farsa de verduras, colócalos en una fuente apta para el horno. Riega con la salsa de setas e introduce en el horno a 190º C, durante 6 minutos.</li>
                    
