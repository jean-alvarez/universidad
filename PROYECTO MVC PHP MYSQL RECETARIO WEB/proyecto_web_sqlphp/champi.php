        <div id="padre">
            <div id="izquierda">
                <ol>
                <p><u>Ingredientes</u></p>
                <li type="disc">600 gramos de Champiñones</li>
                <li type="disc">2 chalotas</li>
                <li type="disc">12 ajos frescos</li>
                <li type="disc">4 huevos</li>
                <li type="disc">25 gramos de mantequilla</li>
                <li type="disc">harina</li>
                <li type="disc">1 cucharadita de harina de maiz</li>
                <li type="disc">1 vaso de vino blanco</li>
                <li type="disc">200 gramos de nata liquida</li>
                <li type="disc">Aceite de oliva, sal y agua</li>
                <li type="disc">1 rama de perejil</li>
            </ol>
            </div>
            <div id="derecha">
                 <p><strong><u>
                    Preparación:
                    </u>
                </strong></p>
                <ol>
                    <li>Picamos las chalotas y los ajos frescos. Los cortaremos bien finos para que se pochen a la vez. En una sartén con un poco de aceite añadimos las chalotas con los ajos. Mientras se pochan estos ingredientes preparamos los champiñones.</li>
                    <li>Limpiamos bien los champiñones y los laminamos.  Rehogaremos todos los ingredientes durante 10 minutos. Los champiñones son un ingrediente que aporta mucha agua, esperaremos a que vaya reduciendo todo el líquido.</li>
                    <li>Reservaremos dos cucharadas de esta mezcla para decorar. El resto irá directamente a un cazo con el vino blanco
                    . Cuando se haya evaporado el alcohol añadimos agua y dejamos reducir el líquido.</li>
                    <li>Trituraremos todo bien en el vaso de la batidora. Para darle un poco de textura a esta salsa le incorporamos la harina de maíz, diluida con un poco de agua.</li>
                    <li>Cascamos los huevos en un bol con un poco de sal. Batimos bien antes de añadirles la nata líquida y la mezcla de champiñones.</li>
                    <li>Untaremos un molde con mantequilla para que no se pegue la masa. Dispondremos la masa en el molde y pasaremos a hornearlo. Lo mejor para este plato es hacerlo al baño maría. Ponemos en molde en una fuente con un poco de agua.</li>
                    <li>Tardará unos 40 minutos a 180º en estar perfectamente cocinado, es importante dejar que se temple una vez hayan cuajado todos los ingredientes, para poder desmoldar con mayor facilidad.</li>
                    <li>Serviremos con un poco de mayonesa casera en frío y con los champiñones salteados que hemos reservado. Es un plato especial con mucho sabor que combina perfectamente con las tardes de verano.</li>
                </ol>
