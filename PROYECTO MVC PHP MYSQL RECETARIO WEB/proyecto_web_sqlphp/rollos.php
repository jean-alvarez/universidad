            <ol>
                <p><strong><u>
                    Preparación:
                    </u>
                </strong></p>
                <li>Limpiar los espárragos quitándoles la parte más dura y cocer en agua 
                    hirviendo con sal y un chorrito de aceite durante 10 minutos. 
                    Reservar </li>
                <li>Cocer la pasta en agua hirviendo salada hasta que esté al dente</li>
                <li>Colocar sobre las láminas de pasta cocida y escurrida una loncha de queso y tres espárragos y enrollar. 
                    Reservar en una bandeja refractaria</li>
                <li>Triturar los espárragos blancos con la batidora</li>
                <li>En una cacerola, derretir la mantequilla y añadir la harina. 
                    Remover con una varilla e ir añadiendo la leche templada, 
                    sin dejar de batir para que no se formen grumos</li>
                <li>Añadir los espárragos triturados, una pizca de nuez moscada y salpimentar al gusto y remover 
                    hasta conseguir una mezcla homogénea</li>
                <li>Verter la salsa encima de los canelones, espolvorear con queso rallado y gratinar en el horno, 
                    hasta que estén dorados</li>
            </ol>
