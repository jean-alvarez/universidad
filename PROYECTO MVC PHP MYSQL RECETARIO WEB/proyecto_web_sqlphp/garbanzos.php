        <center><p><strong><u>Curry de garbanzos con puré de papa</u></strong></p></center>
        </div>
        <div id="padre">
            <div id="izquierda">
            <ol>
                <p><u>Ingredientes</u></p>
                <li type="disc">200 gramos de garbanzos cocidos</li>
                <li type="disc">1/2 pimiento rojo</li>
                <li type="disc">1/2 calabacín</li>
                <li type="disc">1 cebolla</li>
                <li type="disc">3 dientes de ajo</li>
                <li type="disc">1/3 taza de agua </li>
                <li type="disc">1 cda sopera de curry</li>
                <li type="disc">Una pizca de sal</li>
                <li type="disc">2 patatas</li>
                <li type="disc">Agua</li>
            </ol>
            </div>
            <div id="derecha">
                <ol>
                     <p><strong><u>
                    Preparación:
                    </u>
                    </strong></p>
                    <li>Pelamos los ajos y los partimos por la mitad. Los echamos en una olla junto con un chorrito de aceite de oliva. Encendemos a fuego medio y dejamos tostar.</li>
                    <li>Mientras, pelamos y picamos finamente la cebolla.</li>
                    <li>Cuando el ajo esté tostado, añadimos la cebolla y doramos.</li>
                    <li>Mientras doramos la cebolla, picamos en tiras el 1/2 calabacín y el 1/2 pimiento.</li>
                    <li>Después, cuando la cebolla adquiera un color tostado, añadimos el calabacín y el pimiento y seguimos salteando hasta dorar bien todas las verduras. Si fuera necesario, añadimos más aceite de oliva.</li>
                    <li>Cuando los vegetales estén listos, añadimos los garbanzos ya cocidos (si usas de bote, procura quitar el líquido antes).</li>
                    <li>Salteamos por 5 minutos a fuego medio.</li>
                    <li>Añadimos el 1/3 de taza de salsa de tomate y 1/3 taza de agua (que sea con la misma taza que hemos medido el tomate), mezclamos. Si te gusta un curry de garbanzos con una salsa más líquida, puedes añadir un poco más de agua.</li>
                    <li>Añadimos una pizca de sal y una cucharada sopera de curry como mínimo. Puedes probar y añadir más si lo consideras.</li>
                    <li>Mantenemos a fuego medio por 5 minutos más, removiendo periódicamente.</li>
                    <li>Paralelamente, pelamos dos patatas y cortamos en cubos grandes. Los ponemos a hervir en una olla con abundante agua y las dejaremos cocer hasta que estén suficientemente tiernas como para ser atravesadas con facilidad.</li>
                    <li>Cuando estén listas, las retiramos y machacamos junto con un chorrito de agua y sal, creando así el puré de patata.</li>
                    <li>Colocamos una base de puré de patata en un plato y servimos caliente el curry de garbanzos por encima. ¡Y listo!</li>
                    
                </ol>
